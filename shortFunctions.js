// shorthand functions

const multiply = (a, b)  => a * b; 
console.log(multiply(2, 3));


const add = (a, b)  => a + b; 
console.log(add(2, 3));


const subtract = (a, b)  => a - b;
console.log(subtract(6, 3));


const complex = (a, b, c) => {
    var x = a * b; 
    return x / c;
};
console.log(complex(2, 3, 4));